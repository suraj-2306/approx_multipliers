function [matrix] = reduction_2(ma1)
	ma2 = zeros(4,11);

	%first col
	[ma2(1,2),ma2(1,1)] = comp2_2(ma1(1,5),ma1(2,4));
	ma2(2,1) = ma1(3,3);
	ma2(3,1) = ma1(4,2);
	ma2(4,1) = ma1(5,1);

	%2 col
	[ma2(1,3),ma2(2,2)] = comp4_2(ma1(1,6),ma1(2,5),ma1(3,4),ma1(4,3));
	ma2(3,2) = ma1(5,2);
	ma2(4,2) = ma1(6,1);

	%3rd col
	[ma2(1,4),ma2(2,3)] = comp4_2(ma1(1,7),ma1(2,6),ma1(3,5),ma1(4,4));
	[ma2(2,4),ma2(3,3)] = comp2_2(ma1(5,3),ma1(6,2));
	ma2(4,3) = ma1(7,1);

	%4th col
	[ma2(1,5),ma2(3,4)] = comp4_2(ma1(1,8),ma1(2,7),ma1(3,6),ma1(4,5));
	[ma2(2,5),ma2(4,4)] = comp4_2(ma1(5,4),ma1(6,3),ma1(7,2),ma1(8,1));
	%5th col
	[ma2(1,6),ma2(3,5)] = comp4_2(ma1(2,8),ma1(3,7),ma1(4,6),ma1(5,5))	;
	[ma2(2,6),ma2(4,5)] = comp3_2(ma1(6,4),ma1(7,3),ma1(8,2));

	%6th col
	[ma2(1,7),ma2(3,6)] = comp4_2(ma1(3,8),ma1(4,7),ma1(5,6),ma1(6,5));
	[ma2(2,7),ma2(4,6)] = comp2_2(ma1(7,4),ma1(8,3));

	%7th col
	[ma2(1,8),ma2(3,7)] = comp4_2(ma1(4,8),ma1(5,7),ma1(6,6),ma1(7,5));
	ma2(4,7) = ma1(8,4);

	%8th col
	[ma2(1,9),ma2(2,8)] = comp2_2(ma1(5,8),ma1(6,7));
	ma2(3,8) = ma1(7,6);
	ma2(4,8) = ma1(8,5);

	%9th col
	ma2(2,9) = ma1(6,8);
	ma2(3,9) = ma1(7,7);
	ma2(4,9) = ma1(8,6);

	%10 col
	ma2(1,10) = ma1(7,8);
	ma2(2,10) = ma1(8,7);

	%11th col
	ma2(1,11) = ma1(8,8);

	matrix = ma2;
end
